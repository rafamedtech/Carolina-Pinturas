create table public.sales_orders (
  id uuid primary key default gen_random_uuid(),
  folio integer generated by default as identity unique,
  status_key varchar(32) not null default 'borrador'
    references public.order_statuses(key) on delete restrict on update cascade,
  customer_id uuid not null references public.siigo_customers(id) on delete restrict on update cascade,
  customer_name_snapshot text not null,
  customer_rfc_snapshot text,
  customer_payload jsonb not null,
  order_date date not null default current_date,
  promised_date date,
  observations text,
  currency_code varchar(3) not null default 'MXN',
  subtotal numeric(20, 6) not null default 0,
  discount_total numeric(20, 6) not null default 0,
  tax_total numeric(20, 6) not null default 0,
  total numeric(20, 6) not null default 0,
  siigo_reference text,
  registered_in_siigo_at timestamptz(6),
  created_by_name text not null,
  created_by_email text not null,
  created_by_role varchar(32) not null,
  updated_by_email text not null,
  version integer not null default 1,
  created_at timestamptz(6) not null default now(),
  updated_at timestamptz(6) not null,
  constraint sales_orders_totals_nonnegative check (
    subtotal >= 0 and discount_total >= 0 and tax_total >= 0 and total >= 0
  ),
  constraint sales_orders_version_positive check (version > 0)
);

create index sales_orders_status_key_order_date_idx
  on public.sales_orders (status_key, order_date);
create index sales_orders_customer_id_order_date_idx
  on public.sales_orders (customer_id, order_date);
create index sales_orders_created_at_idx on public.sales_orders (created_at);

create table public.sales_order_items (
  id uuid primary key default gen_random_uuid(),
  order_id uuid not null references public.sales_orders(id) on delete cascade on update cascade,
  position integer not null,
  product_id uuid not null references public.siigo_products(id) on delete restrict on update cascade,
  product_code_snapshot text not null,
  product_name_snapshot text not null,
  product_description_snapshot text,
  product_reference_snapshot text,
  unit_code_snapshot text,
  unit_name_snapshot text,
  product_payload jsonb not null,
  quantity numeric(20, 6) not null,
  unit_price numeric(20, 6) not null,
  discount_percentage numeric(9, 6) not null default 0,
  discount_amount numeric(20, 6) not null default 0,
  tax_payload jsonb not null,
  subtotal numeric(20, 6) not null,
  tax_amount numeric(20, 6) not null default 0,
  total numeric(20, 6) not null,
  created_at timestamptz(6) not null default now(),
  unique (order_id, position),
  constraint sales_order_items_position_positive check (position > 0),
  constraint sales_order_items_quantity_positive check (quantity > 0),
  constraint sales_order_items_amounts_nonnegative check (
    unit_price >= 0
    and discount_percentage between 0 and 100
    and discount_amount >= 0
    and subtotal >= 0
    and tax_amount >= 0
    and total >= 0
  )
);

create index sales_order_items_product_id_idx
  on public.sales_order_items (product_id);

create table public.sales_order_status_history (
  id bigint generated by default as identity primary key,
  order_id uuid not null references public.sales_orders(id) on delete cascade on update cascade,
  from_status_key varchar(32)
    references public.order_statuses(key) on delete restrict on update cascade,
  to_status_key varchar(32) not null
    references public.order_statuses(key) on delete restrict on update cascade,
  note text,
  changed_by_name text not null,
  changed_by_email text not null,
  changed_by_role varchar(32) not null,
  changed_at timestamptz(6) not null default now()
);

create index sales_order_status_history_order_id_changed_at_idx
  on public.sales_order_status_history (order_id, changed_at);

