create table public.order_statuses (
  key varchar(32) primary key,
  label varchar(64) not null,
  sort_order integer not null unique,
  color varchar(24) not null default 'neutral',
  is_terminal boolean not null default false,
  is_active boolean not null default true,
  created_at timestamptz(6) not null default now(),
  updated_at timestamptz(6) not null
);

create table public.siigo_products (
  id uuid primary key,
  code varchar(100) not null,
  name text not null,
  account_group_id integer,
  account_group_name text,
  type varchar(64),
  stock_control boolean,
  active boolean,
  tax_included boolean,
  unit_code varchar(32),
  unit_name text,
  key_code varchar(32),
  key_name text,
  reference text,
  description text,
  barcode text,
  brand text,
  available_quantity numeric(20, 6),
  siigo_created_at timestamptz(6),
  siigo_updated_at timestamptz(6),
  raw_payload jsonb not null,
  synced_at timestamptz(6) not null default now(),
  created_at timestamptz(6) not null default now(),
  updated_at timestamptz(6) not null
);

create index siigo_products_code_idx on public.siigo_products (code);
create index siigo_products_name_idx on public.siigo_products (name);

create table public.siigo_product_prices (
  id bigint generated by default as identity primary key,
  product_id uuid not null references public.siigo_products(id) on delete cascade on update cascade,
  currency_code varchar(3) not null,
  position integer not null,
  name text,
  value numeric(20, 6) not null,
  raw_payload jsonb not null,
  unique (product_id, currency_code, position)
);

create table public.siigo_product_taxes (
  product_id uuid not null references public.siigo_products(id) on delete cascade on update cascade,
  siigo_tax_id integer not null,
  name text,
  type varchar(32),
  percentage numeric(9, 6),
  raw_payload jsonb not null,
  primary key (product_id, siigo_tax_id)
);

create table public.siigo_product_warehouses (
  product_id uuid not null references public.siigo_products(id) on delete cascade on update cascade,
  siigo_warehouse_id integer not null,
  name text,
  quantity numeric(20, 6),
  raw_payload jsonb not null,
  primary key (product_id, siigo_warehouse_id)
);

create table public.siigo_product_components (
  id bigint generated by default as identity primary key,
  product_id uuid not null references public.siigo_products(id) on delete cascade on update cascade,
  siigo_component_id text,
  name text,
  quantity numeric(20, 6),
  raw_payload jsonb not null
);

create index siigo_product_components_product_id_idx
  on public.siigo_product_components (product_id);

