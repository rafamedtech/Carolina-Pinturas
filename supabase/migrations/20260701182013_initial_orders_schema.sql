create table public.siigo_customer_contacts (
  id bigint generated by default as identity primary key,
  customer_id uuid not null references public.siigo_customers(id) on delete cascade on update cascade,
  position integer not null,
  first_name text,
  last_name text,
  email text,
  phone text,
  raw_payload jsonb not null,
  unique (customer_id, position)
